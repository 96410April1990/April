package trainingSelenium;

public class LearnAggregation {
	
	String name;
	int age;
	String sex;
	String city;
	String state;
	String country;
	
	LearnAggregation(String na, int ag, String se, String ci, String st, String co) {
		
		this.name = na;
		this.age = ag;
		this.sex = se;
		this.city = ci;
		this.state = st;
		this.country = co;
		
	}

}
